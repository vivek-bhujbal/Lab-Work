# Star Printing
for i in range(1,5):
    for j in range(1,i+1):
        print("*",end=" ")
    print()

print("------------")

# Number Printing
for i in range(1,5):
    for j in range(1,i+1):
        print(j,end=" ")
    print()

print("------------")

